    private void bAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bAceptarActionPerformed
        try {
            validarDatos();
            
            if (operacion == 1)
                alta();
            else
                modificar();
        }
        catch (DatoRequerido e) {
            JOptionPane.showMessageDialog(this, "falta introducir datos");
        }
        catch (DatoNoValido e) {
            JOptionPane.showMessageDialog(this, "los datos introducidos son incorrectos");
        }
        catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "se encontro numero pero fallo al convertir");
        }
        catch (DateTimeParseException e) {
            JOptionPane.showMessageDialog(this, "error en el formato de de fecha u hora");
        }
        catch (MySQLIntegrityConstraintViolationException e) {
            JOptionPane.showMessageDialog(this, "un evento con ese nombre ya existe");
        }
        catch(ErrorBaseDatos e) {
            JOptionPane.showMessageDialog(this, "no se pudo actualizar base de datos debido a un error desconocido");
        }
        catch (Exception e) {
            System.out.println(e.getClass());
        }
    }//GEN-LAST:event_bAceptarActionPerformed

