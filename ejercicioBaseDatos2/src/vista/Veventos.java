    private void alta() throws Exception {
            
            DateTimeFormatter formatoFecha = DateTimeFormatter.ofPattern("dd/MM/yyyy");
            LocalDate fecha = LocalDate.parse(tfFecha.getText(),formatoFecha);
            
            DateTimeFormatter formatoHora = DateTimeFormatter.ofPattern("HH:mm");
            LocalTime horaInicio = LocalTime.parse(tfHoraInicio.getText(), formatoHora);
            LocalTime horaFin = LocalTime.parse(tfHoraFin.getText(), formatoHora);
            
            int max = Integer.parseInt(tfMaxPersonas.getText());
            
            EjercicioBaseDatos2.altaEventos(tfNombre.getText(), tfLugar.getText(), fecha, horaInicio, horaFin, max);
            JOptionPane.showMessageDialog(this, "operacion(alta) realizada correctamente");
    }
    
    private void modificar() throws Exception {
        
        DateTimeFormatter formatoFecha = DateTimeFormatter.ofPattern("dd/MM/yyyy");
        LocalDate fecha = LocalDate.parse(tfFecha.getText(),formatoFecha);
        
        DateTimeFormatter formatoHora = DateTimeFormatter.ofPattern("HH:mm");
        LocalTime horaInicio = LocalTime.parse(tfHoraInicio.getText(), formatoHora);
        LocalTime horaFin = LocalTime.parse(tfHoraFin.getText(), formatoHora);
        
        int max = Integer.parseInt(tfMaxPersonas.getText());
        
        int res = EjercicioBaseDatos2.modificarEvento(tfLugar.getText(), fecha, horaInicio, horaFin, max);
        JOptionPane.showMessageDialog(this, "operacion(modificar) realizada correctamente, se modificaron "+res+" filas");
        this.dispose();
    }
     
    private void validarDatos() throws Exception{
        
        // mirar si esta vacio
        if (tfNombre.getText().isEmpty() || tfLugar.getText().isEmpty() || tfFecha.getText().isEmpty() || 
            tfHoraInicio.getText().isEmpty() || tfHoraFin.getText().isEmpty() || tfMaxPersonas.getText().isEmpty())
            throw new DatoRequerido();
        
        // pattern y matcher
        Pattern patt = Pattern.compile("[a-zA-Z0-9 ]*");
        Matcher matt = patt.matcher(tfNombre.getText());
        if (!matt.matches())
            throw new DatoNoValido();
        
        matt = patt.matcher(tfLugar.getText());
        if (!matt.matches())
            throw new DatoNoValido();
        
        patt = Pattern.compile("[0-9]*");
        matt = patt.matcher(tfMaxPersonas.getText());
        if (!matt.matches() || tfMaxPersonas.getText().length() > 3 ||tfMaxPersonas.getText().length() <= 0)
            throw new DatoNoValido();
        
    }
