    private void validar(String nombre) throws Exception{
        
        if (nombre.isEmpty())
            throw new DatoRequerido();
            
        Pattern patt = Pattern.compile("[a-zA-Z0-9 ]*");
        Matcher matt = patt.matcher(nombre);
        if (!matt.matches())
            throw new DatoNoValido();
            
    }
