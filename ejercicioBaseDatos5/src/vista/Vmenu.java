    private void miBorrarEventoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_miBorrarEventoActionPerformed
        try {
            String nombre = JOptionPane.showInputDialog(this, "introduce nombre del evento a borrar");
            validar(nombre);
            
            EjercicioBaseDatos5.borrarEvento(nombre);
            
            JOptionPane.showMessageDialog(this, "operacion realizada con exito");
        }
        catch (DatoRequerido e) {
            JOptionPane.showMessageDialog(this, "no has introducido nada");
        }
        catch (DatoNoValido e) {
            JOptionPane.showMessageDialog(this, "el nombre no vale");
        }
        catch (NonexistentEntityException e) {
            JOptionPane.showMessageDialog(this, "no se encontro el evento");
        }
        catch(Exception e) {
            System.out.println(e.getClass()+""+e.getCause());
        }
    }//GEN-LAST:event_miBorrarEventoActionPerformed

    private void miModificarEventoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_miModificarEventoActionPerformed
        try {
            String nombre = JOptionPane.showInputDialog(this, "introduce nombre del evento a modificar");
            validar(nombre);
            
            if (!EjercicioBaseDatos5.buscarEvento(nombre))
                throw new FilaNoEncontrada();
            
            EjercicioBaseDatos5.vOperacionConDatos(nombre);
        }
        catch (DatoRequerido e) {
            JOptionPane.showMessageDialog(this, "no has introducido nada");
        }
        catch (DatoNoValido e) {
            JOptionPane.showMessageDialog(this, "el nombre no vale");
        }
        catch (FilaNoEncontrada e) {
            JOptionPane.showMessageDialog(this, "no se encontro el evento");
        }
        catch(Exception e) {
            System.out.println(e.getClass());
        }
    }//GEN-LAST:event_miModificarEventoActionPerformed

