    private void bAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bAceptarActionPerformed
        try {
            if (operacion.equals("alta")) {
                //validaciones
                if (tfNombre.getText().isEmpty() || tfEdad.getText().isEmpty() || tfProfesion.getText().isEmpty() || tfTelefono.getText().isEmpty())
                    throw new Excepcion("todos los datos mostrados son obligatorios y falta alguno para rellenar");
                
                
                
                EjercicioBaseDatos.insertar(tfNombre.getText(),Integer.parseInt(tfEdad.getText()),tfProfesion.getText(),Integer.parseInt(tfTelefono.getText()));
                JOptionPane.showMessageDialog(this, "persona a√±adida con exito");
            }
            else {
                EjercicioBaseDatos.volver();
            }
        }
        catch (Excepcion e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
        catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "problemas con los datos numericos");
        }
        catch (MySQLIntegrityConstraintViolationException e) {
            JOptionPane.showMessageDialog(this, "problemas: hay una persona que ya tiene ese nombre");
        }
        catch(Exception e) {
            System.out.println(e.getMessage()+e.getClass());
        }
    }//GEN-LAST:event_bAceptarActionPerformed

    private void bAnteriorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bAnteriorActionPerformed
        
        if (!(indiceActual == 0)) {
            indiceActual --;
            tfNombre.setText(lista.get(indiceActual).getNombre());
            tfEdad.setText(String.valueOf(lista.get(indiceActual).getEdad()));
            tfProfesion.setText(lista.get(indiceActual).getProfesion());
            tfTelefono.setText(String.valueOf(lista.get(indiceActual).getTelefono()));
        }
    }//GEN-LAST:event_bAnteriorActionPerformed

    private void bPosteriorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bPosteriorActionPerformed
        
        if (!(indiceActual == lista.size()-1)) {
            indiceActual ++;
            tfNombre.setText(lista.get(indiceActual).getNombre());
            tfEdad.setText(String.valueOf(lista.get(indiceActual).getEdad()));
            tfProfesion.setText(lista.get(indiceActual).getProfesion());
            tfTelefono.setText(String.valueOf(lista.get(indiceActual).getTelefono()));
        }
    }//GEN-LAST:event_bPosteriorActionPerformed

